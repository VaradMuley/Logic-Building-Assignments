#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints numbers from 5 down to 1 using recursion.
//  Input         : ---
//  Output        : Void
//  Time Complexity : O(N)
//
////////////////////////////////////////////////////////////

void Display()
{
    static int iCnt = 5;

    if(iCnt >= 1)
    {
        cout << iCnt << "\t";
        iCnt--;
        Display();
    }
}

////////////////////////////////////////////////////////////
//
//  ENTRY POINT FUNCTION
//
////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

