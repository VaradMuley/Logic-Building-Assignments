#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints characters from A to F using recursion.
//  Input         : ---
//  Output        : Void
//  Time Complexity : O(N)
//
////////////////////////////////////////////////////////////

void Display()
{
    static char iCnt = 'A';

    if(iCnt <= 'F')
    {
        cout << iCnt << "\t";
        iCnt++;
        Display();
    }
}

////////////////////////////////////////////////////////////
//
//  ENTRY POINT FUNCTION
//
////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

