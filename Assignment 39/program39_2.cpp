#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints numbers from 1 to 5 using recursion.
//  Input         : ---
//  Output        : Void
//  Time Complexity : O(N)
//
////////////////////////////////////////////////////////////

void Display()
{
    static int iCnt = 1;

    if(iCnt <= 5)
    {
        cout << iCnt << "\t";
        iCnt++;
        Display();
    }
}

////////////////////////////////////////////////////////////
//
//  ENTRY POINT FUNCTION
//
////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

