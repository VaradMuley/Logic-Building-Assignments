#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints characters from a to f using recursion.
//  Input         : ---
//  Output        : Void
//  Time Complexity : O(N)
//
////////////////////////////////////////////////////////////

void Display()
{
    static char iCnt = 'a';

    if(iCnt <= 'f')
    {
        cout << iCnt << "\t";
        iCnt++;
        Display();
    }
}

////////////////////////////////////////////////////////////
//
//  ENTRY POINT FUNCTION
//
////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

