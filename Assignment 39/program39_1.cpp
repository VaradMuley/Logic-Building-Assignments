#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints '*' symbol using recursion.
//  Input         : ---
//  Output        : Void
//  Time Complexity : O(N)
//
////////////////////////////////////////////////////////////

void Display()
{
    static int iCnt = 1;

    if(iCnt <= 5)
    {
        cout << "*\t";
        iCnt++;
        Display();
    }
}

////////////////////////////////////////////////////////////
//
//  ENTRY POINT FUNCTION
//
////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}

