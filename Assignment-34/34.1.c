#include <stdio.h>

////////////////////////////////////////////////////////////
//
//  Function Name : DisplayASCII
//  Description :   Displays ASCII values with characters
//  Input :         void
//  Output :        void
//
////////////////////////////////////////////////////////////

void DisplayASCII()
{
    int iCnt = 0;

    printf("ASCII Table (0 to 255):\n");

    for(iCnt = 0; iCnt <= 255; iCnt++)
    {
        printf("%d  ->  %c\n", iCnt, iCnt);
    }
}

////////////////////////////////////////////////////////////
//
//  ENTRY POINT FUNCTION
//
////////////////////////////////////////////////////////////

int main()
{
    DisplayASCII();

    return 0;
}

